// Arrays are just a list of items 
public class Arrays {

	public static void main(String[] args) {
		System.out.println("Practice using arrays");
		System.out.println("---------------------");
		
		System.out.println("A list of fruit");
		System.out.println("---------------------");

		String [] food = { "Apple", "Banana", "Pear"};
		
		// Arrays index starts at zero 
		System.out.println(food[0]);
		System.out.println(food[1]);
		System.out.println(food[2]);
		
		// Set up a for loop to list all items 
		
		for ( int i = 0; i<3; i++) {
			System.out.println(food[i]);
		}
		
		// Look into applications of arrays in gaming 
		}

}
