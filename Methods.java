// Practice using Methods 
public class Methods {

	public static void main(String[] args) {
		addition(5,10);
		multiply(5,10);
		

	}
	
	public static void addition (int a, int b) {
		System.out.println(a+b);
		
	}
	
	public static void multiply ( int a, int b) {
		System.out.println(a*b);
		
	}

}
