// EscapeSequences
public class EscapeSequences {

	public static void main(String[] args) {
		
		// Double quote
		System.out.println("\"Hello\" World");
		System.out.println("-------------------");

		
		// Single backslash 
		System.out.println("\\");
		System.out.println("-------------------");
		
		// Put a tab between Hello and World and !
		System.out.println("Hello \t World \t!");
		System.out.println("-------------------");

		// Put onto a new line  
		System.out.println("Hello \n World \n!");
		System.out.println("-------------------");
		
		// Note: Can add as many as you want 
		 
		// Double tab example 
		System.out.println("Hello \t \t World \t!");
		System.out.println("-------------------");

		// Double new line example 1 
		System.out.println("Hello\nWorld\n!");
		System.out.println("-------------------");
	
		// Double new line example 2 
		System.out.println("\n Hello \n World \n !");
		System.out.println("-------------------");
					
	}

}
